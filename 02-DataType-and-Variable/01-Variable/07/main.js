const country = "Thailand";
const continent = "Asia";
console.log(`number is ${2}`); // 2, number
console.log(`result is ${1 + 3}`); // 4, number
console.log(`I live in ${country}`); // Thailand, string
console.log(`I live in ${country}, ${continent}`); // Thailand, Asia, string
console.log(`I live in ${country + ", " + continent}`); // Thailand, Asia, string
console.log(`I live in ${"country, continent"}`); // country, continent, string